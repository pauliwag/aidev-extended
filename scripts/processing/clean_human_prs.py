# ============================================================================
# Loading
# ============================================================================


def load_entries(path: Path) -> list[dict]:
    """Load all entries from a JSONL file, skipping malformed lines."""
    if not path.exists():
        raise FileNotFoundError(f"{path} not found")

    entries = []
    skipped = 0
    with open(path, "r", encoding="utf-8") as f:
        for line_num, line in enumerate(f, 1):
            if not line.strip():
                continue
            try:
                entries.append(json.loads(line))
            except json.JSONDecodeError as e:
                skipped += 1
                print(f"  WARNING: Skipped malformed line {line_num}: {e}")
                if skipped <= 3:
                    print(f"    Content preview: {line[:120]}...")

    if skipped:
        print(f"  Skipped {skipped} malformed lines out of {skipped + len(entries)} total")

    return entries


