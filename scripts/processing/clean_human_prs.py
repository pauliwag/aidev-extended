# Discovery Mode
# ============================================================================


def discover_suspicious_authors(entries: list[dict], top_n: int = 30) -> None:
    """Print top PR authors to help identify bot accounts."""
    author_counts = Counter(e.get("author", "unknown") for e in entries)

    print(f"Top {top_n} authors by PR count:\n")
    print(f"  {'Author':<35} {'PRs':>6}  {'In blocklist?'}")
    print(f"  {'-'*35} {'-'*6}  {'-'*13}")

    for author, count in author_counts.most_common(top_n):
        blocked = "YES" if author.lower() in BOT_USERNAMES else ""
        print(f"  {author:<35} {count:>6}  {blocked}")

    # Also flag authors that appear in many distinct repos
    author_repos: dict[str, set] = {}
    for e in entries:
        author = e.get("author", "unknown")
        repo = e.get("repo_full_name", "unknown")
        author_repos.setdefault(author, set()).add(repo)

    print(f"\n\nAuthors active in 20+ repos (likely bots):\n")
    print(f"  {'Author':<35} {'PRs':>6}  {'Repos':>6}  {'In blocklist?'}")
    print(f"  {'-'*35} {'-'*6}  {'-'*6}  {'-'*13}")

    suspicious = [
        (author, author_counts[author], len(repos))
        for author, repos in author_repos.items()
        if len(repos) >= 20
    ]
    suspicious.sort(key=lambda x: x[2], reverse=True)

    for author, pr_count, repo_count in suspicious:
        blocked = "YES" if author.lower() in BOT_USERNAMES else ""
        print(f"  {author:<35} {pr_count:>6}  {repo_count:>6}  {blocked}")

    not_blocked = [a for a, _, _ in suspicious if a.lower() not in BOT_USERNAMES]
    if not_blocked:
        print(f"\n  Review these {len(not_blocked)} unblocked multi-repo authors.")
        print(f"  Add confirmed bots to BOT_USERNAMES and re-run.")


# ============================================================================
# Cleaning Logic
# ============================================================================


def check_bot_username(pr: dict) -> str | None:
    """Return reason string if author is a known bot, else None."""
    author = pr.get("author", "")
    if author.lower() in BOT_USERNAMES:
        return f"bot_username:{author}"
    return None


def check_ai_generated(pr: dict) -> str | None:
    """Return reason string if PR body indicates agent-generated PR, else None."""
    body = pr.get("body") or ""
    body_lower = body.lower()
    for marker in AI_GENERATED_MARKERS:
        if marker in body_lower:
            return f"ai_generated:{marker}"
    return None


def clean_human_prs(dry_run: bool = False) -> dict:
    """
    Clean human PR file in-place, quarantining removed entries.

    Returns summary statistics.
    """
    entries = load_entries(HUMAN_PR_FILE)
    print(f"Loaded {len(entries):,} human PRs from {HUMAN_PR_FILE.name}")

    # Classify each entry
    clean = []
    excluded = []

    for pr in entries:
        reason = check_bot_username(pr) or check_ai_generated(pr)
        if reason:
            pr["_exclusion_reason"] = reason
            excluded.append(pr)
        else:
            clean.append(pr)

    # Summary
    bot_count = sum(
        1 for e in excluded if e["_exclusion_reason"].startswith("bot_username:")
    )
    ai_count = sum(
        1 for e in excluded if e["_exclusion_reason"].startswith("ai_generated:")
    )

    print(f"\nResults:")
    print(f"  Clean:                  {len(clean):,}")
    print(f"  Excluded (total):       {len(excluded):,}")
    print(f"    - Bot usernames:      {bot_count:,}")
    print(f"    - AI-generated leaks: {ai_count:,}")

    if excluded:
        print(f"\nExcluded entries:")
        for e in excluded:
            title = e.get("title", "?")[:80]
            line = f"  [{e['_exclusion_reason']}] {e.get('author', '?')} - {title}"
            print(line.encode("ascii", errors="replace").decode("ascii"))

    if dry_run:
        print("\n[DRY RUN] No files modified.")
        return {
            "total": len(entries),
            "clean": len(clean),
            "excluded": len(excluded),
            "bot_count": bot_count,
            "ai_count": ai_count,
        }

    # Backup original file (skip if backup already exists from a previous run)
    backup_path = HUMAN_PR_FILE.with_suffix(".jsonl.bak")
    if not backup_path.exists():
        shutil.copy2(HUMAN_PR_FILE, backup_path)
        print(f"\nBackup saved to {backup_path.name}")
    else:
        print(f"\nBackup already exists at {backup_path.name} (skipping)")

    # Write clean entries back to original file
    with open(HUMAN_PR_FILE, "w", encoding="utf-8") as f:
        for pr in clean:
            json.dump(pr, f)
            f.write("\n")
    print(f"Wrote {len(clean):,} clean PRs to {HUMAN_PR_FILE.name}")

    # Write excluded entries to quarantine
    QUARANTINE_DIR.mkdir(parents=True, exist_ok=True)
    mode = "a" if QUARANTINE_FILE.exists() else "w"
    with open(QUARANTINE_FILE, mode, encoding="utf-8") as f:
        for pr in excluded:
            json.dump(pr, f)
            f.write("\n")
    print(f"Wrote {len(excluded):,} excluded PRs to {QUARANTINE_FILE.name} (mode={mode})")

    # Verify
    with open(HUMAN_PR_FILE, "r", encoding="utf-8") as f:
        verify_count = sum(1 for line in f if line.strip())

    if verify_count == len(clean):
        print(f"\nVerification passed: {verify_count:,} lines")
    else:
        print(f"\nVerification FAILED: expected {len(clean):,}, got {verify_count:,}")

    return {
        "total": len(entries),
        "clean": len(clean),
        "excluded": len(excluded),
        "bot_count": bot_count,
        "ai_count": ai_count,
    }


